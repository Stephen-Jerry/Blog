package javaBean;
/**排序Bean类*/

public class Sort {
    private String sortname;
    private int sortuserid;
    private String sortusername;

    public String getSortusername() {
        return sortusername;
    }

    public void setSortusername(String sortusername) {
        this.sortusername = sortusername;
    }

    public String getSortname() {
        return sortname;
    }

    public void setSortname(String sortname) {
        this.sortname = sortname;
    }

    public int getSortuserid() {
        return sortuserid;
    }

    public void setSortuserid(int sortuserid) {
        this.sortuserid = sortuserid;
    }


}
